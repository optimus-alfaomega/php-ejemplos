<?php
	// Ejemplo de Bucles en  PHP - https://www.dinapyme.com
	// Ejemplo sobre FOR con dos contadores
	// Creamos un bucle con For para mostrar por pantalla del 1 al 10 y del 10 al 1.
	
	for ($contador1 = 1, $contador2 = 10; $contador1 <=10, $contador2 >=1; $contador1++, $contador2--){
		echo "$contador1  --  $contador2<br />";
	}
	
?>