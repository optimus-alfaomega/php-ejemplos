<?php
	// Ejemplo de Bucles en  PHP - https://www.dinapyme.com
	// Ejemplo sobre Operador Do While
	// Creamos un buclie con Do While hasta que el número sea 10.
	// Dentro del white incrementamos el valor del contador en 1. Se muestra el valor de $cotador en cada iteración.
	
	$contador = 1;  // Valor inicial para la variable $contador
	
	do {
		echo $contador . "<br />";
		$contador++;
	}
	while ($contador <= 10)  // Mientras $contador sea menor o igual que 10
	
?>