<?php
	// Ejemplo de funciones en PHP.
	// Creamos una función que realiza una suma de dos números y devuelve el resultado mediente Return.
	// En este caso realizamos la suma en la misma línea que return.
		
	function sumarDosNumeros ()
	{
		return 5 + 3;
	}	
	
	// A continuación llamamos a la función anterior para que muestre el texto por pantalla.
	echo ("El resultado de la suma de 5 + 3 es: " . sumarDosNumeros() );
	
?>