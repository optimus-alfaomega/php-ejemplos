<?php
	// Ejemplo de operadores y expresiones PHP - https://www.dinapyme.com
	// Operadores de comparación. ==
	// En este ejemplo dos pruebas con el operador ==.
	
	$numero = 10;  // variable para los ejemeplos
	
	// Título
	echo "<center><h1>Operadores de Comparación - Igual (==)</h1></center>";
	
	// mostrar si el contenido de la variable $número es 10.
	echo "Comprobar si \$numero es igual a 10: ";
	echo ($numero == 10);  // Muestra 1 porque es igual.
	echo "<br />";
	
	// mostrar si el contenido de la variable $numero es 5
	echo "Comprobar si \$numero es igual a 5: ";
	echo ($numero == 5);  // No muestra 1 porque es diferente
	
?>